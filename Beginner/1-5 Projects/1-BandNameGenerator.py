# This is a simple band name generator program.

# Step 1: Greeting
print("Hi! Welcome to the Band Name Generator.")

# Step 2: Asking the user for input
city = input("Which city did you grow up in? ")
pet = input("What is the name of your pet? ")

# Step 3: Generating the band name
band_name = city + " " + pet

# Step 4: Displaying the band name to the user
print("Your band name could be:", band_name)

# Step 5: Adding a new line for better formatting
print()

# Step 6: Exiting the program
print("Thank you for using the Band Name Generator. Have a great day!")
